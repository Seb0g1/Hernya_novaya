"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

const items = [
  { href: "/dashboard", label: "Обзор", icon: LayoutIcon },
  { href: "/dashboard/accounts", label: "Аккаунты", icon: UsersIcon },
  { href: "/dashboard/media", label: "Медиа", icon: MediaIcon },
  { href: "/dashboard/publish", label: "Публикация", icon: SendIcon },
  { href: "/dashboard/jobs", label: "Задачи", icon: ListIcon },
];

export function DashboardNav() {
  const pathname = usePathname();

  return (
    <nav className="flex flex-1 flex-col gap-0.5 px-3 py-4">
      {items.map((item) => {
        const active =
          item.href === "/dashboard"
            ? pathname === "/dashboard"
            : pathname === item.href || pathname.startsWith(`${item.href}/`);
        const Icon = item.icon;
        return (
          <Link
            key={item.href}
            href={item.href}
            className={`group flex items-center gap-3 rounded-xl px-3 py-2.5 text-sm font-medium transition-colors ${
              active
                ? "bg-white/10 text-white shadow-sm"
                : "text-slate-400 hover:bg-white/5 hover:text-slate-200"
            }`}
          >
            <Icon
              className={`h-[18px] w-[18px] shrink-0 ${
                active ? "text-indigo-300" : "text-slate-500 group-hover:text-slate-400"
              }`}
            />
            {item.label}
          </Link>
        );
      })}
    </nav>
  );
}

